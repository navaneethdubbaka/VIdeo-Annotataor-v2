def frequency_sanity_check(imu_samples):
    t0 = imu_samples[0]["timestamp_ns"]
    t1 = imu_samples[-1]["timestamp_ns"]

    duration = (t1 - t0) * 1e-9
    N = len(imu_samples)

    freq = N / duration

    print("Sanity Frequency:", freq)
