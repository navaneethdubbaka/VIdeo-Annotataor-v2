"""**1. Reading .imu (Binary → Structured Data)**


Header = 64 bytes

Each sample = 76 bytes

Timestamp = uint64 (nanoseconds)

Data = float32 values


✅ Already in SI units

✅ Already fused (quat available)

✅ Already gravity removed (lin_accel)
"""

import struct
import numpy as np

def read_imu(file_path):
    with open(file_path, "rb") as f:
        # --- Read header (64 bytes) ---
        header = f.read(64)

        magic = header[:8].decode()
        version = struct.unpack("<I", header[8:12])[0]
        sample_rate = struct.unpack("<I", header[12:16])[0]

        start_time_ns = struct.unpack("<Q", header[24:32])[0]

        print("Magic:", magic)
        print("Version:", version)
        print("Configured Sample Rate:", sample_rate)

        # --- Read samples ---
        samples = []

        while True:
            data = f.read(76)
            if len(data) < 76:
                break

            unpacked = struct.unpack("<Q 3f 3f 3f f 4f 3f", data)

            sample = {
                "timestamp_ns": unpacked[0],
                "accel": unpacked[1:4],
                "gyro": unpacked[4:7],
                "mag": unpacked[7:10],
                "temp": unpacked[10],
                "quat": unpacked[11:15],
                "lin_accel": unpacked[15:18]
            }

            samples.append(sample)

    return samples

samples = read_imu(file_path_imu)

