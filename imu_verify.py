"""**2. Reading .vts (Frame ↔ Timestamp mapping)**


Header = 32 bytes

Entry = 12 bytes

frame_number → uint32

timestamp_ns → uint64
"""

file_path_vts = "/content/recording1.vts"

def read_vts(file_path):
    with open(file_path, "rb") as f:
        header = f.read(32)

        entries = []

        while True:
            data = f.read(12)
            if len(data) < 12:
                break

            frame_number, timestamp_ns = struct.unpack("<IQ", data)

            entries.append({
                "frame": frame_number,
                "timestamp_ns": timestamp_ns
            })

    return entries

