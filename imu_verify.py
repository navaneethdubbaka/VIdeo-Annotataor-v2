"""**Compute IMU Frequency**"""

def compute_frequency(imu_samples):
    timestamps = np.array([s["timestamp_ns"] for s in imu_samples])

    # Convert to seconds
    timestamps = timestamps * 1e-9

    # Differences
    dt = np.diff(timestamps)

    avg_dt = np.mean(dt)

    freq = 1.0 / avg_dt

    print("Average dt:", avg_dt)
    print("Computed Frequency:", freq)

    return freq

